function nakshatraTransitParagraph(transitMoonNak: string): string {
  const adv = NAK_MOON_TRANSIT_ADVICE[transitMoonNak] ?? DEFAULT_MOON_TRANSIT_ADVICE;
  const intro = `Транзитная Луна сейчас в накшатре «${transitMoonNak}». В джйотиш это описывают как суточный лунный фон дня.`;
  return [
    intro,
    `Самочувствие: ${adv.wellbeing}`,
    `Питание — делайте: ${adv.nutritionDo}`,
    `Питание — не делайте: ${adv.nutritionAvoid}`,
  ].join("\n\n");
}

function natalWeaveParagraph(
  natal: NatalProfile,
  snap: Snap,
  natalMoonD1: string,
  natalSunD1: string,
): string {
  const transitMoonNak = nakName(snap.moon);
  const transitSunNak = nakName(snap.sun);
  const iNatM = nakIndex(natal.moon);
  const iTrM = nakIndex(snap.moon);
  const sep = nakSeparationIndex(iNatM, iTrM);

  const sunAdv = NAK_SUN_TRANSIT_ADVICE[transitSunNak] ?? DEFAULT_SUN_TRANSIT_ADVICE;
  const sunLines: string[] = [
    `Натальное Солнце — накшатра «${natalSunD1}». Транзитное Солнце сегодня в накшатре «${transitSunNak}» — в джйотиш это описывают как суточный солнечный фон: дневной тонус и то, как организм держит ритм основного приёма пищи.`,
    `Самочувствие: ${sunAdv.wellbeing}`,
    `Питание — делайте: ${sunAdv.nutritionDo}`,
    `Питание — не делайте: ${sunAdv.nutritionAvoid}`,
  ];
  if (transitSunNak === natalSunD1) {
    sunLines.push(
      "Сегодня натальная и транзитная накшатры Солнца совпадают: проще удерживать время обеда и вечернее правило без срывов.",
    );
  }

  const parts: string[] = [sunLines.join("\n\n")];

  if (transitMoonNak === natalMoonD1) {
    parts.push(
      `Натальная Луна — «${natalMoonD1}». Сегодня Луна снова в этой накшатре: обычно острее реакция на сон, объём жидкости и размер порций.`,
    );
  } else if (sep === 13 || sep === 14) {
    parts.push(
      `Натальная Луна — «${natalMoonD1}». Сегодня Луна примерно напротив натальной на кругу из 27: чаще усиливаются тяжесть после еды, сонливость и чувствительность к соли — обед и вечер держите строже обычного.`,
    );
  } else if (sep <= 3) {
    parts.push(
      `Натальная Луна — «${natalMoonD1}». Сегодня Луна рядом с натальной на том же кругу — день ближе к привычному для вас лунному тону; время приёма пищи не сдвигайте.`,
    );
  }

  return parts.join("\n\n");
}

export interface NatalDayForecast {
  /** Заголовок блока на экране «Сегодня». */
  title: string;
  /** Полное имя титхи (как в UI). */
  tithi_label: string;
  /** Абзацы: титхи → транзит Луны → Солнце (натал + транзит, самочувствие/питание) и при необходимости натальная Луна vs сегодняшняя без повтора названия транзитной накшатры Луны → Меркурий, Венера, Сатурн. */
  paragraphs: string[];
}

/**
 * Прогноз по натальной карте (профиль NATAL) на календарную дату: титхи + транзит Луны в накшатре + Солнце (натал/транзит, самочувствие и питание) + натальная Луна относительно сегодняшней + транзиты Меркурия, Венеры, Сатурна к наталу.
 * Полностью детерминированный текст, без LLM.
 */
export function buildNatalDayForecast(
  natal: NatalProfile,
  snap: Snap,
  tithiNameRu: string,
): NatalDayForecast {
  const natalMoonD1 = nakshatraD1(natal.moon);
  const natalSunD1 = nakshatraD1(natal.sun);
  const transitMoonNak = nakName(snap.moon);

  const natalTithiNum = natalBirthTithiNumber(natal);

  let p1 = `Сегодня титхи «${tithiNameRu}» (лунный день ${snap.tithi}). ${tithiForecastParagraph(snap.tithi)}`;

  if (isRiktaTithi(natalTithiNum)) {
    if (isRiktaTithi(snap.tithi)) {
      p1 += ` ${paragraphRiktaTodayStacked()}`;
    }
  } else if (isRiktaTithi(snap.tithi)) {
    p1 +=
      " Сегодня рикта-титхи: в панчанге день без удачи для новых начинаний; для питания — без новых продуктов и без расширения порций, только расчётный протокол и устойчивое время приёмов.";
  }

  const p2 = nakshatraTransitParagraph(transitMoonNak);
  const p3 = natalWeaveParagraph(natal, snap, natalMoonD1, natalSunD1);
  const grahaPs = buildGrahaTransitParagraphs(natal, snap);

  return {
    title: "Прогноз по вашей натальной карте",
    tithi_label: tithiNameRu,
    paragraphs: [p1, p2, p3, ...grahaPs],
  };
}
