def pick_meal(matrix: MealMatrix, day_of_year: int) -> MealOption:
    opts = _MATRICES[matrix]
    return opts[day_of_year % len(opts)]


def all_meals_for_matrix(matrix: MealMatrix) -> list[MealOption]:
    return list(_MATRICES[matrix])
